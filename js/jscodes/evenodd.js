document.write("Even Numbers: <br>");

for (let i = 1; i <= 10; i++) {
    if (i % 2 == 0) {
        document.write(i + "<br>");
    }
}

document.write("<br><br>");

document.write("Odd Numbers: <br>");

for (let i = 1; i <= 10; i++) {
    if (i % 2 == 1) {
        document.write(i + "<br>");
    }
}